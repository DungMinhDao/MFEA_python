def sphere1(x):
    """y=(x0 - 1)^2 + (x1 - 2)^2 + (x2 - 3)^2"""
    return (x[0] - 1)**2 + (x[1] - 2)**2 + (x[2] - 3)**2 + 10 

def sphere2(x):
    """y=(x0 + 1)^2 + (x1 - 1)^2"""
    return (x[0] + 1)**2 + (x[1] - 1)**2 + 5

def sphere3(x):
    """y=(x0 + 1)^2 + (x1 - 1)^2 + (x2 - 3)^2 + 6"""
    return (x[0] + 1)**2 + (x[1] - 1)**2 + (x[2] - 6)**2 + 6